    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        
        Cifrars d = new Cifrars();
        try {
            
            if(ingreso = true){
                 clavef= d.convertStringToSecretKeyto(this.jTextField1.getText());
                 claves = this.jTextField1.getText();
            }else if (ingreso= false){
                 clavef = clave;   
            }
            
            if(clavef != null){
                System.out.println(clavef);
             path = this.jTextField2.getText();
             FUR = d.Descifrararch(clavef, path);
                System.out.println(FUR);
            if (FUR == true){
             jTextField3.setText("Cifrado exitosamente");
             jTextField4.setText("clave: " + claves);
             
            }else{
                 jTextField3.setText("Cifrado sin exito");
                 jTextField4.setText("Revisar la clave");
            } 
            }else{
                jTextField3.setText("Cifrado sin exito"); 
                jTextField4.setText("Revisar la clave");
            }
           
           
        } catch (Exception ex) {
            Logger.getLogger(Cifrar.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_jButton3ActionPerformed

